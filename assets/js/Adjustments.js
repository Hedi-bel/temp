// Function to update the page content
function updateContent(lang) {
    document.querySelectorAll("[data-translate]").forEach(element => {
        const key = element.getAttribute("data-translate");
        element.textContent = translations[lang][key] || translations["en"][key]; // Fallback to English
    });

    // Update the selected language text
    document.getElementById("current-language").textContent = lang === "en" ? "English" : lang === "fr" ? "francais" : "Arabic";
}

// Event listener for language switcher
document.querySelectorAll(".language ul li a").forEach(link => {
    link.addEventListener("click", (e) => {
        e.preventDefault();
        const selectedLang = link.getAttribute("data-lang");
        updateContent(selectedLang);

        // Save the selected language in localStorage
        localStorage.setItem("selectedLanguage", selectedLang);
    });
});

// Load the saved language on page load
window.addEventListener("load", () => {
    const savedLang = localStorage.getItem("selectedLanguage") || "fr"; // Default to French
    updateContent(savedLang);
});
